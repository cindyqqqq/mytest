# 导入数据处理库pandas，别名pd（数据分析常用配置）
import pandas as pd
# 导入Streamlit库，别名st（Streamlit框架标准用法，用于快速构建Web应用）
import streamlit as st

# 设置页面主标题（header级别字体，比title稍小，适合作为页面核心标题）
st.header('学生 小满-数字档案')

# ---------------------- 基础信息模块 ----------------------
# 子标题+图标，标识基础信息区域（subheader用于区分不同功能模块）
st.subheader('📌基础信息')
# 学生ID信息（caption字体较小，适合次要补充信息）
st.caption('学生ID:NEO-2025-001')
# 注册时间和精神状态（使用:green[]语法设置绿色文本，增强视觉区分）
st.markdown(' 注册时间：:green[2025-12-18 15：09：10]|精神状态:✅ 正常')
# 当前位置和安全等级（绿色文本突出重要信息，符合"绝密"等级的视觉提示）
st.markdown(' 当前位置：:green[实训楼710]|安全等级：:green[绝密]')  # 修正原代码笔误（注册时间重复）

# ---------------------- 技能矩阵模块 ----------------------
# 子标题+图标，标识技能矩阵区域
st.subheader("📊 技能矩阵")
# 创建3列布局（columns(3)实现三等分列，用于并列展示不同技能）
col1, col2, col3 = st.columns(3)

# 第一列：C语言技能信息
with col1:
    st.caption('C语言')  # 技能名称（次要文本）
    st.markdown('#### 95%')  # 技能熟练度（大号字体突出显示）
    st.write(":green[↑ 2%]")  # 技能变化趋势（绿色表示提升）

# 第二列：Python技能信息
with col2:
    st.caption('Python')  # 技能名称
    st.markdown('#### 87%')  # 技能熟练度
    st.write(":red[↓ -1%]")  # 技能变化趋势（红色表示下降）

# 第三列：Java技能信息
with col3:
    st.caption('Java')  # 技能名称
    st.markdown('#### 68%')  # 技能熟练度
    st.write(":red[↓ -10%]")  # 技能变化趋势（红色表示下降）

# ---------------------- 课程进度模块 ----------------------
# 子标题+图标，标识课程进度区域
st.subheader("📚 Streamlit课程进度")
# 补充说明文本（小号字体，明确进度条含义）
st.markdown('###### Streamlit课程进度')
# 进度条组件（参数60表示60%进度，Streamlit默认蓝色进度条）
st.progress(60)

# ---------------------- 任务日志模块 ----------------------
# 子标题+图标，标识任务日志区域
st.subheader("📋 任务日志")
# 定义任务日志数据（字典格式，key为列名，value为对应列数据）
data = {
    '日期':["2023-10-01", "2023-10-12", "2023-10-20"],  # 任务执行日期
    '任务':["学生数字档案", "课程管理系统", "数据可视化"],  # 任务名称
    '状态':["✅ 完成", "● 进行中", "✕ 未完成"],  # 任务当前状态（带图标更直观）
    '难度':["★★★☆☆", "★★☆☆☆", "★★★★☆"],  # 任务难度星级
}
# 定义表格索引（Series类型，name为空字符串表示索引列无标题）
index = pd.Series(['0', '1', '2'], name='')
# 创建DataFrame（pandas数据结构，用于组织表格数据）
df = pd.DataFrame(data, index=index)
# 渲染表格（st.table()用于展示静态表格，适合少量数据）
st.table(df)  # 修正原代码错误（原传入data，改为df才能显示自定义索引）

# 注：原代码中单独定义的月份索引未使用，保留作为备用（如需扩展月度数据可启用）
